media_anual = float(input("Qual foi sua média anual? "))

if (media_anual >= 6.0 and media_anual <= 10.0):
    print("Você foi aprovado!")
elif (media_anual >= 3.0 and media_anual <= 5.9):
    print("Você foi reprovado!")
elif (media_anual >= 0.0 and media_anual <= 2.9):
    print("Retido para exame.")



